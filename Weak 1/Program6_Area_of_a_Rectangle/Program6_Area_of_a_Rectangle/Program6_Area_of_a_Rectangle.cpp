#include <iostream>
using namespace std;

int main()
{
    // Create variables for width and height
    int width = 15;
    int height = 25;

    // Calculate the area
    int area = width * height;

    // Output the result
    cout << "The area of a " << width << " by " << height << " rectangle is " << area << endl;

    return 0;
}
